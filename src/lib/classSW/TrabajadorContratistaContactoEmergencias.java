package lib.classSW;

public class TrabajadorContratistaContactoEmergencias {
	private int idTrabajadorContratista;
	private int idContactoEmergencia;
	private String nombre;
	private String telefono;
	private String celular;
	private String email;
	private int parentesco;
	public int getIdTrabajadorContratista() {
		return idTrabajadorContratista;
	}
	public void setIdTrabajadorContratista(int idTrabajadorContratista) {
		this.idTrabajadorContratista = idTrabajadorContratista;
	}
	public int getIdContactoEmergencia() {
		return idContactoEmergencia;
	}
	public void setIdContactoEmergencia(int idContactoEmergencia) {
		this.idContactoEmergencia = idContactoEmergencia;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getCelular() {
		return celular;
	}
	public void setCelular(String celular) {
		this.celular = celular;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getParentesco() {
		return parentesco;
	}
	public void setParentesco(int parentesco) {
		this.parentesco = parentesco;
	}

	
}
