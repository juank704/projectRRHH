package lib.classSW;

public class Carga {
	char tramoCarga;
	int montoCarga;
	int periodoCarga;
	
	public char getTramoCarga() {
		return tramoCarga;
	}
	public void setTramoCarga(char tramoCarga) {
		this.tramoCarga = tramoCarga;
	}
	public int getMontoCarga() {
		return montoCarga;
	}
	public void setMontoCarga(int montoCarga) {
		this.montoCarga = montoCarga;
	}
	public int getPeriodoCarga() {
		return periodoCarga;
	}
	public void setPeriodoCarga(int periodoCarga) {
		this.periodoCarga = periodoCarga;
	} 
	
	
	
}
