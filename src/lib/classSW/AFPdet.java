package lib.classSW;

public class AFPdet {
	String nombreAFP;
	int qttyAFP;
	String periodoAFP;
	int sumPeriodos;
	
	
	public int getSumPeriodos() {
		return sumPeriodos;
	}
	public void setSumPeriodos(int sumPeriodos) {
		this.sumPeriodos = sumPeriodos;
	}
	public String getNombreAFP() {
		return nombreAFP;
	}
	public void setNombreAFP(String nombreAFP) {
		this.nombreAFP = nombreAFP;
	}
	public int getQttyAFP() {
		return qttyAFP;
	}
	public void setQttyAFP(int qtty) {
		this.qttyAFP = qtty;
	}
	public String getPeriodoAFP() {
		return periodoAFP;
	}
	public void setPeriodoAFP(String periodoAFP) {
		this.periodoAFP = periodoAFP;
	}
	
	
}
