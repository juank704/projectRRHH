package lib.classSW;

public class haberesDescuentos {
	public int codigo;
	public String descripcion;
	public String tipo;
	public String imponible;
	public String tributable;
	public String gratificacion;
	public String incluirGlosa;
	public String caracterFija;
	public String caracterVariable;
	public String semanaCorrida;
	
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getImponible() {
		return imponible;
	}
	public void setImponible(String imponible) {
		this.imponible = imponible;
	}
	public String getTributable() {
		return tributable;
	}
	public void setTributable(String tributable) {
		this.tributable = tributable;
	}
	public String getGratificacion() {
		return gratificacion;
	}
	public void setGratificacion(String gratificacion) {
		this.gratificacion = gratificacion;
	}
	public String getIncluirGlosa() {
		return incluirGlosa;
	}
	public void setIncluirGlosa(String incluirGlosa) {
		this.incluirGlosa = incluirGlosa;
	}
	public String getCaracterFija() {
		return caracterFija;
	}
	public void setCaracterFija(String caracterFija) {
		this.caracterFija = caracterFija;
	}
	public String getCaracterVariable() {
		return caracterVariable;
	}
	public void setCaracterVariable(String caracterVariable) {
		this.caracterVariable = caracterVariable;
	}
	public String getSemanaCorrida() {
		return semanaCorrida;
	}
	public void setSemanaCorrida(String semanaCorrida) {
		this.semanaCorrida = semanaCorrida;
	}
	
	
}
