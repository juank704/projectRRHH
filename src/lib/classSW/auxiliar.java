package lib.classSW;

public class auxiliar {
	
	public int id_aux;
	public String campo;
	public int valor;
	
	
	public int getId_aux() {
		return id_aux;
	}
	public void setId_aux(int id_aux) {
		this.id_aux = id_aux;
	}
	public String getCampo() {
		return campo;
	}
	public void setCampo(String campo) {
		this.campo = campo;
	}
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}

}
