package lib.classSW;

public class tiposMovimiento {

	
	public int idTiposMovimiento;
	public String nombre;
	public String descripcion;
	
	public int getIdTiposMovimiento() {
		return idTiposMovimiento;
	}
	public void setIdTiposMovimiento(int idTiposMovimientos) {
		this.idTiposMovimiento = idTiposMovimientos;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descipcion) {
		this.descripcion = descipcion;
	}
	
	
	
	
}
