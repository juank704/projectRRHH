package lib.classSW;

public class EQUIPO {
	public int id;
	public String codigo_equipo;
	public String codigo_bloque;
	public String descripcion;
	public String codigo_campo;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCodigo_equipo() {
		return codigo_equipo;
	}
	public void setCodigo_equipo(String codigo_equipo) {
		this.codigo_equipo = codigo_equipo;
	}
	public String getCodigo_bloque() {
		return codigo_bloque;
	}
	public void setCodigo_bloque(String codigo_bloque) {
		this.codigo_bloque = codigo_bloque;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getCodigo_campo() {
		return codigo_campo;
	}
	public void setCodigo_campo(String codigo_campo) {
		this.codigo_campo = codigo_campo;
	}
}
