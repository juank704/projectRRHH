package lib.classSW;

public class evaporacionAcumulada {
	public int codigo;
	public String campo;
	public String hasta_verde;
	public String desde_verde;
	public String hasta_amarillo;
	public String desde_amarillo;
	public String hasta_rojo;
	public String desde_rojo;
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getCampo() {
		return campo;
	}
	public void setCampo(String campo) {
		this.campo = campo;
	}
	public String getHasta_verde() {
		return hasta_verde;
	}
	public void setHasta_verde(String hasta_verde) {
		this.hasta_verde = hasta_verde;
	}
	public String getDesde_verde() {
		return desde_verde;
	}
	public void setDesde_verde(String desde_verde) {
		this.desde_verde = desde_verde;
	}
	public String getHasta_amarillo() {
		return hasta_amarillo;
	}
	public void setHasta_amarillo(String hasta_amarillo) {
		this.hasta_amarillo = hasta_amarillo;
	}
	public String getDesde_amarillo() {
		return desde_amarillo;
	}
	public void setDesde_amarillo(String desde_amarillo) {
		this.desde_amarillo = desde_amarillo;
	}
	public String getHasta_rojo() {
		return hasta_rojo;
	}
	public void setHasta_rojo(String hasta_rojo) {
		this.hasta_rojo = hasta_rojo;
	}
	public String getDesde_rojo() {
		return desde_rojo;
	}
	public void setDesde_rojo(String desde_rojo) {
		this.desde_rojo = desde_rojo;
	}
}
