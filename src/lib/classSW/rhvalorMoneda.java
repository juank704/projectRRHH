	public String idMoneda;
	public double valor;
	public String fecha;
	public int getIdValor() {
		return idValor;
	}
	public void setIdValor(int idValor) {
		this.idValor = idValor;
	}
	public String getIdMoneda() {
		return idMoneda;
	}
