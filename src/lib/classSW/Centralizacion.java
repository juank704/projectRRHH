package lib.classSW;

public class Centralizacion {

	private int id_centralizacion;
	private int periodo;
	private String id_sociedad;
	private String fecha_envio;
	private String sap;
	private String entrada_sap;
	private String salida_sap;
	private String fecha_proceso;
	
	public int getId_centralizacion() {
		return id_centralizacion;
	}
	public void setId_centralizacion(int id_centralizacion) {
		this.id_centralizacion = id_centralizacion;
	}
	public int getPeriodo() {
		return periodo;
	}
	public void setPeriodo(int periodo) {
		this.periodo = periodo;
	}
	public String getId_sociedad() {
		return id_sociedad;
	}
	public void setId_sociedad(String id_sociedad) {
		this.id_sociedad = id_sociedad;
	}
	public String getFecha_envio() {
		return fecha_envio;
	}
	public void setFecha_envio(String fecha_envio) {
		this.fecha_envio = fecha_envio;
	}
	public String getSap() {
		return sap;
	}
	public void setSap(String sap) {
		this.sap = sap;
	}
	public String getEntrada_sap() {
		return entrada_sap;
	}
	public void setEntrada_sap(String entrada_sap) {
		this.entrada_sap = entrada_sap;
	}
	public String getSalida_sap() {
		return salida_sap;
	}
	public void setSalida_sap(String salida_sap) {
		this.salida_sap = salida_sap;
	}
	public String getFecha_proceso() {
		return fecha_proceso;
	}
	public void setFecha_proceso(String fecha_proceso) {
		this.fecha_proceso = fecha_proceso;
	}
	

	
	
	
}
