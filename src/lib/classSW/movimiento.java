package lib.classSW;

public class movimiento {

	int idMovimiento;
	int idTrabajador;
	int idTiposMovimiento;
	String fechaIngreso;
	String fechaTermino;
	int idContrato;
	int sociedad;
	
	
	
	public int getSociedad() {
		return sociedad;
	}
	public void setSociedad(int sociedad) {
		this.sociedad = sociedad;
	}
	public int getIdMovimiento() {
		return idMovimiento;
	}
	public void setIdMovimiento(int idMovimiento) {
		this.idMovimiento = idMovimiento;
	}
	public int getIdTrabajador() {
		return idTrabajador;
	}
	public void setIdTrabajador(int idTrabajador) {
		this.idTrabajador = idTrabajador;
	}
	public int getIdTiposMovimiento() {
		return idTiposMovimiento;
	}
	public void setIdTiposMovimiento(int idTiposMovimiento) {
		this.idTiposMovimiento = idTiposMovimiento;
	}
	public String getFechaIngreso() {
		return fechaIngreso;
	}
	public void setFechaIngreso(String fechaIngreso) {
		this.fechaIngreso = fechaIngreso;
	}
	public String getFechaTermino() {
		return fechaTermino;
	}
	public void setFechaTermino(String fechaTermino) {
		this.fechaTermino = fechaTermino;
	}
	public int getIdContrato() {
		return idContrato;
	}
	public void setIdContrato(int idContrato) {
		this.idContrato = idContrato;
	}
	
	
	
	
	
	
}
