package lib.classSW;

public class Moneda {

	public int idMoneda;
	public String fecha;
	public double tasaUF;
	public double tasaUTM;
	public double sueldoMinimo;
	public double topeAFP;
	public double topeAFC;
	
	public int getIdMoneda() {
		return idMoneda;
	}
	public void setIdMoneda(int idMoneda) {
		this.idMoneda = idMoneda;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public double getTasaUF() {
		return tasaUF;
	}
	public void setTasaUF(double tasaUF) {
		this.tasaUF = tasaUF;
	}
	public double getTasaUTM() {
		return tasaUTM;
	}
	public void setTasaUTM(double tasaUTM) {
		this.tasaUTM = tasaUTM;
	}
	public double getSueldoMinimo() {
		return sueldoMinimo;
	}
	public void setSueldoMinimo(double sueldoMinimo) {
		this.sueldoMinimo = sueldoMinimo;
	}
	public double getTopeAFP() {
		return topeAFP;
	}
	public void setTopeAFP(double topeAFP) {
		this.topeAFP = topeAFP;
	}
	public double getTopeAFC() {
		return topeAFC;
	}
	public void setTopeAFC(double topeAFC) {
		this.topeAFC = topeAFC;
	}
	
	
	
	
	
}
