package lib.classSW;

public class ListaSociedad {
	public String sociedad;
	public String denominacionSociedad;
	public int idSociedad;
	public String getSociedad() {
		return sociedad;
	}
	public void setSociedad(String sociedad) {
		this.sociedad = sociedad;
	}
	public String getDenominacionSociedad() {
		return denominacionSociedad;
	}
	public void setDenominacionSociedad(String denominacionSociedad) {
		this.denominacionSociedad = denominacionSociedad;
	}
	public int getIdSociedad() {
		return idSociedad;
	}
	public void setIdSociedad(int idSociedad) {
		this.idSociedad = idSociedad;
	}
	
	

}
