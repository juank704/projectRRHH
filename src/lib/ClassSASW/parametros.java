package lib.ClassSASW;

public class parametros {
	public int id;
	public String codigo;
	public String llave;
	public String descripcion;
	public int activo;
	public String codPrevired;
	public String rutParametro;
	public String codSap;
	
	
	public String getCodSap() {
		return codSap;
	}
	public void setCodSap(String codSap) {
		this.codSap = codSap;
	}
	public String getCodPrevired() {
		return codPrevired;
	}
	public void setCodPrevired(String codPrevired) {
		this.codPrevired = codPrevired;
	}
	public String getRutParametro() {
		return rutParametro;
	}
	public void setRutParametro(String rutParametro) {
		this.rutParametro = rutParametro;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getLlave() {
		return llave;
	}
	public void setLlave(String llave) {
		this.llave = llave;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public int getActivo() {
		return activo;
	}
	public void setActivo(int activo) {
		this.activo = activo;
	}
	

}
