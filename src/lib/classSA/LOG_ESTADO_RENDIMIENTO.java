package lib.classSA;

public class LOG_ESTADO_RENDIMIENTO {

	public int codigo;
	public int rendimiento;
	public int estado_rendimiento;
	public String fecha;
	public int usuario;
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public int getRendimiento() {
		return rendimiento;
	}
	public void setRendimiento(int rendimiento) {
		this.rendimiento = rendimiento;
	}
	public int getEstado_rendimiento() {
		return estado_rendimiento;
	}
	public void setEstado_rendimiento(int estado_rendimiento) {
		this.estado_rendimiento = estado_rendimiento;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public int getUsuario() {
		return usuario;
	}
	public void setUsuario(int usuario) {
		this.usuario = usuario;
	}
	
	
}
