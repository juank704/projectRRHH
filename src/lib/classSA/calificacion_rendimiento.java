package lib.classSA;

public class calificacion_rendimiento {
	
	public int codigo;
	public int trabajado;
	public int rendimiento;
	public int promedio_cuadrilla;
	public int calificacion;
	public int caudrilla;
	public int labor;
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public int getTrabajado() {
		return trabajado;
	}
	public void setTrabajado(int trabajado) {
		this.trabajado = trabajado;
	}
	public int getRendimiento() {
		return rendimiento;
	}
	public void setRendimiento(int rendimiento) {
		this.rendimiento = rendimiento;
	}
	public int getPromedio_cuadrilla() {
		return promedio_cuadrilla;
	}
	public void setPromedio_cuadrilla(int promedio_cuadrilla) {
		this.promedio_cuadrilla = promedio_cuadrilla;
	}
	public int getCalificacion() {
		return calificacion;
	}
	public void setCalificacion(int calificacion) {
		this.calificacion = calificacion;
	}
	public int getCaudrilla() {
		return caudrilla;
	}
	public void setCaudrilla(int caudrilla) {
		this.caudrilla = caudrilla;
	}
	public int getLabor() {
		return labor;
	}
	public void setLabor(int labor) {
		this.labor = labor;
	}
	
	

}
