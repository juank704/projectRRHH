package lib.classSA;

public class CUARTEL_CONFIRMACION {

	public int codigo;
	public int codigo_confirmacion;
	public int cuartel;
	public int has_reales;
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public int getCodigo_confirmacion() {
		return codigo_confirmacion;
	}
	public void setCodigo_confirmacion(int codigo_confirmacion) {
		this.codigo_confirmacion = codigo_confirmacion;
	}
	public int getCuartel() {
		return cuartel;
	}
	public void setCuartel(int cuartel) {
		this.cuartel = cuartel;
	}
	public int getHas_reales() {
		return has_reales;
	}
	public void setHas_reales(int has_reales) {
		this.has_reales = has_reales;
	}
	
	
}
