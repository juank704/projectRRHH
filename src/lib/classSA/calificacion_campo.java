package lib.classSA;

public class calificacion_campo {

	public int codigo;
	public String campo;
	public float bajo_max;
	public float promedio_max;
	public float bueno_max;
	public int labor;
	public int especie;
	public int faena;
	
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getCampo() {
		return campo;
	}
	public void setCampo(String campo) {
		this.campo = campo;
	}
	public float getBajo_max() {
		return bajo_max;
	}
	public void setBajo_max(float bajo_max) {
		this.bajo_max = bajo_max;
	}
	public float getPromedio_max() {
		return promedio_max;
	}
	public void setPromedio_max(float promedio_max) {
		this.promedio_max = promedio_max;
	}
	public float getBueno_max() {
		return bueno_max;
	}
	public void setBueno_max(float bueno_max) {
		this.bueno_max = bueno_max;
	}
	public int getLabor() {
		return labor;
	}
	public void setLabor(int labor) {
		this.labor = labor;
	}
	public int getEspecie() {
		return especie;
	}
	public void setEspecie(int especie) {
		this.especie = especie;
	}
	public int getFaena() {
		return faena;
	}
	public void setFaena(int faena) {
		this.faena = faena;
	}
	
	
}
