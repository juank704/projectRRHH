package lib.classSA;

public class log_login {
	public int codigo;
	public int id_user;
	public String hora_fecha;
	public int n_ingreso;
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public int getId_user() {
		return id_user;
	}
	public void setId_user(int id_user) {
		this.id_user = id_user;
	}
	public String getHora_fecha() {
		return hora_fecha;
	}
	public void setHora_fecha(String hora_fecha) {
		this.hora_fecha = hora_fecha;
	}
	public int getN_ingreso() {
		return n_ingreso;
	}
	public void setN_ingreso(int n_ingreso) {
		this.n_ingreso = n_ingreso;
	}
	
}
