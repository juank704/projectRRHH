package lib.classSA;

public class bloqueo_periodo {
	public int id;
	public int empresa;
	public String campo;
	public int periodo;
	public int work_agro;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getEmpresa() {
		return empresa;
	}
	public void setEmpresa(int empresa) {
		this.empresa = empresa;
	}
	public String getCampo() {
		return campo;
	}
	public void setCampo(String campo) {
		this.campo = campo;
	}
	public int getPeriodo() {
		return periodo;
	}
	public void setPeriodo(int periodo) {
		this.periodo = periodo;
	}
	public int getWork_agro() {
		return work_agro;
	}
	public void setWork_agro(int work_agro) {
		this.work_agro = work_agro;
	}
	
}
