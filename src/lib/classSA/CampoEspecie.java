package lib.classSA;

public class CampoEspecie {
	public int codigo;
	public String codigo_campo;
	public int codigo_especie;
	public int activo;
	public String nombre_especie;
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getCodigo_campo() {
		return codigo_campo;
	}
	public void setCodigo_campo(String codigo_campo) {
		this.codigo_campo = codigo_campo;
	}
	public int getCodigo_especie() {
		return codigo_especie;
	}
	public void setCodigo_especie(int codigo_especie) {
		this.codigo_especie = codigo_especie;
	}
	public int getActivo() {
		return activo;
	}
	public void setActivo(int activo) {
		this.activo = activo;
	}
	public String getNombre_especie() {
		return nombre_especie;
	}
	public void setNombre_especie(String nombre_especie) {
		this.nombre_especie = nombre_especie;
	}
	
}
