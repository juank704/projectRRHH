package lib.classSA;

public class Campo_Variedad {
	public int codigo;
	public String codigo_campo;
	public String codigo_especie;
	public String activo;
	public String nombre_especie;
	public String codigo_variedad;
	public String variedad;
	
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getCodigo_campo() {
		return codigo_campo;
	}
	public void setCodigo_campo(String codigo_campo) {
		this.codigo_campo = codigo_campo;
	}
	public String getCodigo_especie() {
		return codigo_especie;
	}
	public void setCodigo_especie(String codigo_especie) {
		this.codigo_especie = codigo_especie;
	}
	public String getActivo() {
		return activo;
	}
	public void setActivo(String activo) {
		this.activo = activo;
	}
	public String getNombre_especie() {
		return nombre_especie;
	}
	public void setNombre_especie(String nombre_especie) {
		this.nombre_especie = nombre_especie;
	}
	public String getCodigo_variedad() {
		return codigo_variedad;
	}
	public void setCodigo_variedad(String codigo_variedad) {
		this.codigo_variedad = codigo_variedad;
	}
	public String getVariedad() {
		return variedad;
	}
	public void setVariedad(String variedad) {
		this.variedad = variedad;
	}
	
	
	
}
