package lib.classSA;

public class reingreso_taller {
	public int id;
	public int ingreso;
	public float horometro_reingreso;
	public int estado_reingreso;
	public String fecha_reingreso;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getIngreso() {
		return ingreso;
	}
	public void setIngreso(int ingreso) {
		this.ingreso = ingreso;
	}
	public float getHorometro_reingreso() {
		return horometro_reingreso;
	}
	public void setHorometro_reingreso(float horometro_reingreso) {
		this.horometro_reingreso = horometro_reingreso;
	}
	public int getEstado_reingreso() {
		return estado_reingreso;
	}
	public void setEstado_reingreso(int estado_reingreso) {
		this.estado_reingreso = estado_reingreso;
	}
	public String getFecha_reingreso() {
		return fecha_reingreso;
	}
	public void setFecha_reingreso(String fecha_reingreso) {
		this.fecha_reingreso = fecha_reingreso;
	}
}
