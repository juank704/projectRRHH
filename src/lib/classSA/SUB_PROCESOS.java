package lib.classSA;

public class SUB_PROCESOS {

	public int codigo;
	public String sistema;
	public String procesos;
	public String sub_procesos;
	
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getSistema() {
		return sistema;
	}
	public void setSistema(String sistema) {
		this.sistema = sistema;
	}
	public String getProcesos() {
		return procesos;
	}
	public void setProcesos(String procesos) {
		this.procesos = procesos;
	}
	public String getSub_procesos() {
		return sub_procesos;
	}
	public void setSub_procesos(String sub_procesos) {
		this.sub_procesos = sub_procesos;
	}
	
	
}
