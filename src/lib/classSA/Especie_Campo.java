package lib.classSA;

public class Especie_Campo {

	public int codigo;
	public String codigo_campo;
	public String codigo_especie;
	public int activo;
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getCodigo_campo() {
		return codigo_campo;
	}
	public void setCodigo_campo(String codigo_campo) {
		this.codigo_campo = codigo_campo;
	}
	public String getCodigo_especie() {
		return codigo_especie;
	}
	public void setCodigo_especie(String codigo_especie) {
		this.codigo_especie = codigo_especie;
	}
	public int getActivo() {
		return activo;
	}
	public void setActivo(int activo) {
		this.activo = activo;
	}
}
