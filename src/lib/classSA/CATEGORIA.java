package lib.classSA;

public class CATEGORIA {

	public int codigo;
	public String categoria;
	public String descripcion;
	public int cod_campo;
	public int cod_especie;
	public int estado;
	
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public int getCod_campo() {
		return cod_campo;
	}
	public void setCod_campo(int cod_campo) {
		this.cod_campo = cod_campo;
	}
	public int getCod_especie() {
		return cod_especie;
	}
	public void setCod_especie(int cod_especie) {
		this.cod_especie = cod_especie;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
}
