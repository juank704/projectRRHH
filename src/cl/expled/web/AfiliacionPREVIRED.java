package cl.expled.web;

public class AfiliacionPREVIRED {
	
	public String nombre;
	public String rut;
	public String dv;
	public String codigoPrevired;
	public String apPaterno;
	public String apMaterno;
	

	public String getRut() {
		return rut;
	}

	public void setRut(String rut) {
		this.rut = rut;
	}

	public String getDv() {
		return dv;
	}

	public void setDv(String dv) {
		this.dv = dv;
	}

	public String getCodigoPrevired() {
		return codigoPrevired;
	}

	public void setCodigoPrevired(String codigoPrevired) {
		this.codigoPrevired = codigoPrevired;
	}

	public String getApPaterno() {
		return apPaterno;
	}

	public void setApPaterno(String apPaterno) {
		this.apPaterno = apPaterno;
	}

	public String getApMaterno() {
		return apMaterno;
	}

	public void setApMaterno(String apMaterno) {
		this.apMaterno = apMaterno;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	

}
